abstract class ForzeSpeciali {

    public abstract String gradoSpeciale();

    public abstract int annoInizioGS();

    public abstract String getNome();

    public abstract int getAnnoNasciata(); 

    public abstract boolean isInForzeSpeciale();

    public abstract void setGradoSpeciale(String s);

    public abstract void setAnnoInizioGS(int annoInizioGS);
}